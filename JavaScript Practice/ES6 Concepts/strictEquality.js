// Loose Equality or == Only Checks Equal Value

if (1 == "1") {
  console.log("I Am Equal");
} else {
  console.log("I Am Not Equal");
}
// Output - I Am Equal

// Strict Equality or === Checks Equal Value and Equal Datatype
if (1 === "1") {
  console.log("I Am Equal");
} else {
  console.log("I Am Not Equal");
}
// Output - I Am Not Equal
