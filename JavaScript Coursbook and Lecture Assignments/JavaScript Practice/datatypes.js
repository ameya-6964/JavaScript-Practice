                                                  //      PRIMITIVE DATA TYPES IN JAVASCRIPT //


// String
const user = "Ameya";
console.log(typeof user); // String


// Number 
const no = 5;
console.log(typeof no); // Number


// Boolean
const isLoggedIn = true;
console.log(typeof isLoggedIn); // Boolean


//Null 
const myMoney = null ;
console.log(typeof myMoney);  //Null


// Undefined (We Cannot Set Undefined To Const)
let myJob ;
console.log(typeof myJob);


//Symbol (Used For Libraries)
const eg = Symbol();
console.log(typeof eg);


